package com.example.e_survey.DatabaseLokal;

public class HasilJawabanKeltan {
    private String namaKelompokTani;
    private String namaKetuaKelompokTani;
    private String luasLahan;
    private String komidtasUtama;
    private String jumlahOrang;

    public String getNamaKelompokTani() {
        return namaKelompokTani;
    }

    public void setNamaKelompokTani(String namaKelompokTani) {
        this.namaKelompokTani = namaKelompokTani;
    }

    public String getNamaKetuaKelompokTani() {
        return namaKetuaKelompokTani;
    }

    public void setNamaKetuaKelompokTani(String namaKetuaKelompokTani) {
        this.namaKetuaKelompokTani = namaKetuaKelompokTani;
    }

    public String getLuasLahan() {
        return luasLahan;
    }

    public void setLuasLahan(String luasLahan) {
        this.luasLahan = luasLahan;
    }

    public String getKomidtasUtama() {
        return komidtasUtama;
    }

    public void setKomidtasUtama(String komidtasUtama) {
        this.komidtasUtama = komidtasUtama;
    }

    public String getJumlahOrang() {
        return jumlahOrang;
    }

    public void setJumlahOrang(String jumlahOrang) {
        this.jumlahOrang = jumlahOrang;
    }
}
